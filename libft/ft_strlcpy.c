#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	src_len;
	size_t	i;

	if (size > 0)
	{
		src_len = 0;
		i = 0;
		while (src[i] && i < size - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
		while (src[src_len])
			src_len++;
		return (src_len);
	}
	else
		return (ft_strlen(src));
}
